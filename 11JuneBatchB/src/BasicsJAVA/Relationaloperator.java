package BasicsJAVA;

public class Relationaloperator {

public static void main(String[] args) {
		
		
int a =7, b =7; // variable declaration & initialization
System.out.println(a ==b );  // (a equal to equal to b). In this result is always in Boolean i.e true or false.
System.out.println(a != b); // (Here != means Not equals to. a is not to equal to b).
System.out.println(a>b);  // ( a is greater than b)
System.out.println(a<b);   // ( a is less than b)
System.out.println(a>=b);  // ( a is greater than or equal to b)
System.out.println(a<=b);   // ( a is less than or equal to b)

		


		
		
		
		
		
		
		
	}
}
